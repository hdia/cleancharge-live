@st.cache_data(ttl=300)
def load_json(path: Path) -> dict:
    if not path.exists():
        return {}

    try:
        with path.open(
            "r",
            encoding="utf-8",
        ) as file:
            return json.load(file)
    except Exception:
        return {}


@st.cache_data(ttl=300)
def load_csv(path: Path) -> pd.DataFrame:
    if not path.exists():
        return pd.DataFrame()

    try:
        return pd.read_csv(path)
    except Exception:
        return pd.DataFrame()


def parse_datetime_series(
    series: pd.Series,
) -> pd.Series:
    parsed = pd.to_datetime(
        series,
        errors="coerce",
    )

    if parsed.dt.tz is None:
        parsed = parsed.dt.tz_localize(
            TZ_NAME,
            ambiguous="infer",
            nonexistent="shift_forward",
        )
    else:
        parsed = parsed.dt.tz_convert(
            TZ_NAME
        )

    return parsed


def latest_evaluation_date(
    scorecard: dict,
) -> str | None:
    """
    Return the latest completed evaluation date.

    Supports the field names used by different scorecard versions.
    """
    operational = scorecard.get(
        "operational_summary",
        {}
    )

    return (
        operational.get(
            "latest_evaluation_date"
        )
        or operational.get(
            "latest_evaluated_forecast_date"
        )
        or operational.get(
            "latest_evaluation"
        )
    )


def latest_evaluation_file(
    scorecard: dict,
    filename: str,
) -> Path | None:
    target_date = latest_evaluation_date(
        scorecard
    )

    if not target_date:
        return None

    path = (
        VALIDATION_DIR
        / target_date
        / filename
    )

    return path if path.exists() else None



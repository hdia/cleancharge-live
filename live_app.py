def system_status_banner(
    status: dict,
) -> None:
    health = str(
        status.get(
            "system_health",
            "unavailable",
        )
    ).lower()

    label = status.get(
        "system_health_label"
    )

    if not label:
        label = {
            "healthy": "Operating normally",
            "degraded": "Operational with warnings",
            "unavailable": "Service unavailable",
        }.get(
            health,
            "Status unavailable",
        )

    if health == "healthy":
        symbol = "🟢"
        css_class = "healthy"
    elif health == "degraded":
        symbol = "🟠"
        css_class = "degraded"
    else:
        symbol = "🔴"
        css_class = "unavailable"

    messages = status.get(
        "health_messages",
        []
    )

    if isinstance(messages, str):
        messages = [messages]

    notes = " ".join(
        str(message)
        for message in messages
        if message
    )

    if not notes:
        notes = (
            "Operational status is based on data freshness, "
            "successful updates and forecast publication."
        )

    st.markdown(
        f"""
        <div class="cc-status cc-status-{css_class}">
            <strong>{symbol} System status: {label}</strong><br>
            <span style="font-size:0.88rem; color:#555;">
                {notes}
            </span>
        </div>
        """,
        unsafe_allow_html=True,
    )



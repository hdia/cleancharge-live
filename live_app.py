def fmt_intensity(value) -> str:
    if value is None or pd.isna(value):
        return "Not available"

    return f"{float(value):,.0f} gCO₂/kWh"


def fmt_kg(value) -> str:
    if value is None or pd.isna(value):
        return "Not available"

    return f"{float(value):,.2f} kg CO₂"


def fmt_percent(
    value,
    decimals: int = 1,
) -> str:
    if value is None or pd.isna(value):
        return "Not available"

    return f"{float(value):.{decimals}f}%"


def fmt_hours(value) -> str:
    if value is None or pd.isna(value):
        return "Not available"

    value = float(value)

    if abs(value - round(value)) < 0.05:
        number = int(round(value))
        unit = "hour" if number == 1 else "hours"
        return f"{number} {unit}"

    return f"{value:.1f} hours"


def fmt_datetime(
    value,
    date_only: bool = False,
) -> str:
    if not value:
        return "Not available"

    try:
        timestamp = pd.Timestamp(value)

        if timestamp.tzinfo is None:
            timestamp = timestamp.tz_localize(
                TZ_NAME
            )
        else:
            timestamp = timestamp.tz_convert(
                TZ_NAME
            )

        if date_only:
            return timestamp.strftime(
                "%A %-d %B %Y"
            )

        return timestamp.strftime(
            "%-d %b %Y, %-I:%M %p"
        )

    except Exception:
        return str(value)


def fmt_time(value) -> str:
    """
    Format a timestamp as Melbourne local time using a
    Windows-safe 12-hour clock format.
    """
    if not value:
        return "Not available"

    try:
        timestamp = pd.Timestamp(value)

        if timestamp.tzinfo is None:
            timestamp = timestamp.tz_localize(
                TZ_NAME
            )
        else:
            timestamp = timestamp.tz_convert(
                TZ_NAME
            )

        return timestamp.strftime(
            "%I:%M %p"
        ).lstrip("0")

    except Exception:
        return str(value)


def safe_mean(
    section: dict,
    metric: str,
):
    return (
        section
        .get(metric, {})
        .get("mean")
    )


def stars_text(stars) -> str:
    if stars is None or pd.isna(stars):
        return "Not yet available"

    count = int(round(float(stars)))
    count = max(0, min(5, count))

    return "★" * count + "☆" * (5 - count)


